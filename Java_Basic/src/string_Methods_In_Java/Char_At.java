package string_Methods_In_Java;

public class Char_At {

	public static void main(String[] args) {
		
		String s1 ="Hello";
		System.out.println("Character at Index 0: " + s1.charAt(0));
		System.out.println("Character at Index 1: " + s1.charAt(1));
		System.out.println("Character at Index 2: " + s1.charAt(2));
		System.out.println("Character at Index 3: " + s1.charAt(3));
		System.out.println("Character at Index 4: " + s1.charAt(4));

	}

}
