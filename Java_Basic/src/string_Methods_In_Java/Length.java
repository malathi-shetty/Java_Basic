package string_Methods_In_Java;

public class Length {

	public static void main(String[] args) {

		String s1="Testing";
		String s2="Testing is good";
		System.out.println("The length of the String is in s1: " + s1.length()); //7
		System.out.println("The length of the String is in s2: " + s2.length()); //15 along with space

	}

}
