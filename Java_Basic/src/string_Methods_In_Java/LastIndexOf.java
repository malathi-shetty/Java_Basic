package string_Methods_In_Java;

public class LastIndexOf {

	public static void main(String[] args) {
		
		String s1="Hello";
		
		System.out.println("Last Index of 'H': " + s1.lastIndexOf('h'));
		System.out.println("Last Index of 'H': " + s1.lastIndexOf('H'));
		System.out.println("Last Index of 'e': " + s1.lastIndexOf('e'));
		System.out.println("Last Index of 'l': " + s1.lastIndexOf('l'));
		System.out.println("Last Index of 'l': " + s1.lastIndexOf('l'));
		System.out.println("Last Index of 'o': " + s1.lastIndexOf('0'));
		System.out.println("Last Index of 'o': " + s1.lastIndexOf('o'));

	}

}
