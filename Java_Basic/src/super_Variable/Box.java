package super_Variable;

public class Box { // Parent Class - Also Known as Super Class
	int height = 900;
	String color = "Pink";
	
	public void display()
	{
		System.out.println("Box Class Height - Super: " + height); // 900 - refers to height in Box
		System.out.println("Box Class Color - Super: " + color); // Brown - refers to color in Box
		
	}
	

}
